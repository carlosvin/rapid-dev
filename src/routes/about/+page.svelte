<h1>About</h1>
<p>This is a proof of concept for a fullstack app using SvelteKit and MongoDB.</p>

<p>
	When you create a Proof of Concept, a pilot, a prototype, normally you want to have a piece of
	software that just works. The main goal is not having a production-ready app at all; it is to
	demonstrate the feasibility and viability of an idea, technology, or product before investing
	significant resources into its development. In this repository, I am exemplifying how easy it is
	to do rapid development with <a href="https://kit.svelte.dev/">SvelteKit</a> (fullstack framework)
	and
	<a href="https://www.mongodb.com">MongoDB</a> as a database.
</p>
